    private void jButton_ActualizarInsumoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ActualizarInsumoActionPerformed

        int validacion = 0;
        String nombre_insumo, marca_insumo, modelo_insumo, repuesto_para, num_serie_insumo;
        int cantidad;
        
        nombre_insumo = txt_nombreInsumo.getText().trim();
        marca_insumo = txt_marcaInsumo.getText().trim();
        modelo_insumo = txt_modeloInsumo.getText().trim();
        repuesto_para = cmb_tipoequipo.getSelectedItem().toString();
        num_serie_insumo = txt_serieInsumo.getText().trim();
        cantidad = Integer.parseInt(txt_cantidadInsumo.getText());
        
        if(nombre_insumo.equals("")){
            txt_nombreInsumo.setBackground(Color.red);
            validacion++;
        }
        
        if(marca_insumo.equals("")){
            txt_nombreInsumo.setBackground(Color.red);
            validacion++;
        }
        
        if(modelo_insumo.equals("")){
            txt_nombreInsumo.setBackground(Color.red);
            validacion++;
        }
        
        if(num_serie_insumo.equals("")){
            txt_nombreInsumo.setBackground(Color.red);
            validacion++;
        }
        
        if(cantidad <= 0){
            txt_cantidadInsumo.setBackground(Color.red);
            validacion++;
        }
        
        if(validacion == 0){
            
            try{
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                    "update insumo set nombre_insumo=?, marca_insumo=?, modelo_insumo=?, repuesto_para=?, num_serie_insumo=?, cantidad=? "
                            + "where id_insumo = '" + IDinsumo + "'");
                
                pst.setString(1, nombre_insumo);
                pst.setString(2, marca_insumo);
                pst.setString(3, modelo_insumo);
                pst.setString(4, repuesto_para);
                pst.setString(5, num_serie_insumo);
                pst.setInt(6, cantidad);

                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                
                txt_nombreInsumo.setBackground(Color.green);
                txt_marcaInsumo.setBackground(Color.green);
                txt_modeloInsumo.setBackground(Color.green);
                txt_serieInsumo.setBackground(Color.green);
                txt_cantidadInsumo.setBackground(Color.green);
                
                JOptionPane.showMessageDialog(null, "Actualización correcta");
                this.dispose();
                
            }catch (SQLException e){
                System.err.println("Error en actualizar equipo " + e);
                JOptionPane.showMessageDialog(null, "¡¡ERROR al actualizar equipo!!, contacte a un administrador.");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "Debes llenar todos los campos");
        }
                

    }//GEN-LAST:event_jButton_ActualizarInsumoActionPerformed

