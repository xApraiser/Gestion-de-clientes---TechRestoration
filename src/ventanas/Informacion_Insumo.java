    public Informacion_Insumo() {
        initComponents();
        user = Login.user;
        IDinsumo = GestionarInsumos.IDinsumo_update;
        
        try{
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
                    "select * from insumo where id_insumo = '" + IDinsumo + "'");

            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                txt_nombreInsumo.setText(rs.getString("nombre_insumo"));
                txt_marcaInsumo.setText(rs.getString("marca_insumo"));
                txt_modeloInsumo.setText(rs.getString("modelo_insumo"));
                cmb_tipoequipo.setSelectedItem(rs.getString("repuesto_para"));
                txt_serieInsumo.setText(rs.getString("num_serie_insumo"));
                txt_cantidadInsumo.setText(rs.getString("cantidad"));
            }
            
        }catch (SQLException e){
            System.err.println("Error al consultar la informacion del equipo" + e);
            JOptionPane.showMessageDialog(null, "Error al mostrar la informacion, contacte a un administrador");
        }
        
        setTitle("Insumo registrado con el ID " + IDinsumo + " - Sesión de " + user);
        setSize(530,430);
        setResizable(false);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        
        ImageIcon wallpaper = new ImageIcon("src/imagenes/wallpapertech.jpg");
        Icon icono = new ImageIcon(wallpaper.getImage().getScaledInstance(jLabel_Wallpaper.getWidth(), jLabel_Wallpaper.getHeight(), Image.SCALE_DEFAULT));
        jLabel_Wallpaper.setIcon(icono);
        this.repaint();
        
    }
    
